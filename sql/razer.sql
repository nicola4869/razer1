SET NAMES UTF8;
DROP DATABASE IF EXISTS razer;
CREATE DATABASE razer CHARSET=UTF8;
USE razer;

#创建用户信息表
CREATE TABLE razer_user(
	uid INT PRIMARY KEY AUTO_INCREMENT,
	uname VARCHAR(32),
	upwd VARCHAR(100),
	uemail VARCHAR(100),
	uaddress VARCHAR(100),
	utel VARCHAR(32),
	uline VARCHAR(32)
);

